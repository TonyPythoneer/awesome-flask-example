"""The mixins are designed for models
"""
from .. import db


class CRUDMixin(object):
    def add(self):
        db.session.add(self)
        db.session.commit()

    def update(self):
        db.session.commit()

    def delete(self):
        db.session.delete(self)
        db.session.commit()
